function elevarAlCubo(num) {
  // Retorna el valor de "num" elevado al cubo.
  // Tu código:

  let numeroArriba = Math.ceil(num) // devuelve el n{umero hacia arriba}

  return numeroArriba**3
}

elevarAlCubo(-5.6)

module.exports = elevarAlCubo;
